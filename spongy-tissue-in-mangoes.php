<p>Spongy tissue in Alphonso mango is caused by the onset of premature germination associated events in the seed of developing fruits. The process of germination is initiated when the fruit is about 70% mature. The malady can be prevented by spraying the 50-60% mature fruits with the formulation developed by us at IIHR, Bengaluru. The concept is to delay seed germination, which will in turn prevent the spongy tissue formation. Factors like high temperature and humidity only contribute to higher incidence of spongy tissue and are not to be confused as causative factors. The real problem is the early beginning of seed germination which leads to spongy tissue formation in Alphonso mango. Interested readers may look up our published papers which are available online for more details</p>
