Alphonso, the most delicious variety of mango (Mangifera Indica) is known for its excellent texture, taste and richness in vitamins and minerals.
Despite its great flavor and taste, this King is not perfect and comes with some internal pulp defect (“Spongy Tissue”).</p>

<h3>We would like to educate you about spongy tissue </h3>

<p>Alphonso has incidents of a physiological disorder known as “Spongy Tissue” causing internal breakdown of the pulp. It is characterized by unripe, acidic, pale yellow/white corky tissue with or without air pockets associated with an unacceptable off-flavor in certain region of the mesocarp (pulp) adjacent to the endocarp (stone).Fruits affected by this disorder do not show any external symptoms and the malady is detected only after cutting the fruits open.(Please see the reference pictures) 
</p>
            
<h3>Can you eat mango with spongy tissue?</h3>
<p>A mango with white stuff inside should still be safe to eat but will probably be less flavorful.<br />
The rest of the fruit, however, can still be perfectly fine and still certainly edible.</p>
