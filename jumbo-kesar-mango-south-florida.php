        <h4>Origin</h4>
        <p>‘Jumbo  Kesar’  is  a  seedling  of  ‘Kesar’mango  originally  grown in Dedakyali village, Gujarat State. Madhavray Savani, a mango farmer from Mota Bhamodra village, started propagating the tree inspired by the improved size of the fruit. The ‘Jumbo Kesar’ started gaining popularity in the region, and many farmers started growing it. Today the Savani’s family has established the Gir Nature Trust, promoting sustainable farming and helping local farmers create a mango corridor using mangos including ‘Jumbo Kesar’ for wildlife migration. The tree was cloned and established at the living collection of Reliance Industries located at Jamnagar, Gujarat, and from there it was introduced to south Florida in 2008.</p>
        <p><strong>Tree.</strong></p>
        <p>The tree is medium sized and can be kept at or below 6 ft. without interfering with fruiting with annual pruning. Grafted trees have a spreading growth habit and dense foliage. Mature leaves are dark green while the new growth has a reddish-brown color. The tree and fruit have some tolerance to wet conditions, although  fruit  quality  suffers  when  rainfall  starts  early.  Prun-ing must occur after harvesting every year to limit tree size. It is  advisable  to  avoid  nitrogenous  fertilizers  and  supplemental  watering until flowering has occurred. The tree flushes twice or three times a year, with 13-inch internodes. Trees start bearing fruit at the age of 3–5 years and are productive compared with other Indian mangos.</p>
        <p><strong>Flowers.</strong></p>
