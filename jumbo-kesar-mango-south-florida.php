     <p>Mangos  have  been  cultivated  in  India  since  ancient  times  and  are  an  important  part  of  the  Indian  culture  and  economy.  India exports nearly 30 varieties of mangos. Important export cultivars include ‘Totapuri’, ‘Alphonso’, ‘Dashehri’, and ‘Kesar’. Mangos  have  been  exported  from  India  to  the  United  States  since an irradiation treatment was approved in 2002. Since then, air-shipments of mangos from India to the United States have led to an increased interest in the production of Indian mango cultivars in south Florida. Attractive per-box prices and quality issues associated with the long-distance transport and the neces-sary postharvest treatment of the imported product have made the production of these cultivars in the Western Hemisphere an opportunity for growers (Campbell and Ledesma, 2013).</p>  
     <p>There is little reliable information regarding the most suitable locations for the commercial production of Indian mangos in south Florida  as  they  are  notorious  for  their  horticultural  challenges  here.  Indian  cultivars  have  been  introduced  to  south  Florida  over the decades with little success. There is some experience with ‘Kesar’ mango grown by local farmers in south Florida, but low productivity and large tree size have caused problems. The objective of this paper is to discuss ‘Jumbo Kesar’ as a candidate cultivar for south Florida, based on observations of ‘Jumbo Kesar’ trees grown in a local commercial orchard. The trees have been under evaluation for fruit and flower characteristics since they were planted in 2008. The authors recommend further evaluations with more details for growth management and productivity of ‘Jumbo  Kesar’ in south  Florida. </p>      
