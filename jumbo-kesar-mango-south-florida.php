        <p>The  fruit  are  ready  for  harvest  from June to July in south Florida. Harvesting should be done when the fruit are mature but not ripe. The best flavor is obtained when the fruit is harvested at 25% maturity and stored at room temperature.  Postharvest  life  is  short  at  ambient  temperatures,  but low temperatures can ruin the flavor. </p>
