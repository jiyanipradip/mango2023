<strong class="hdbold">How to choose a Mango</strong>
  <ol>
  <li>Don't focus on color. It is not the best indicator of ripeness.</li>
  <li>Squeeze the mango gently. A ripe mango will give slightly.</li>
  <li>Use your experience with produce such as peaches or avocados, which also become softer as they ripen.</li>
  <li>Ripe Mangos will sometimes have a fruity aroma at their stem ends.</li>
  <li>The red color that appears on some varieties is not an indicator of ripeness. Always judge by feel.</li>
  </ol>
          
  <strong class="hdbold">Ripening and Storing Mangos</strong>
  <ol>
  <li>Keep unripe mangos at room temperature. Mangos shouldn't be refrigerated before they are ripe.</li>
  <li>Mangos will continue to ripen at room temperature becoming sweeter and softer over several days.</li>
  <li>To speed up ripening, place mangos in paper bag at room temperature.</li>
  <li>Once ripe, mangos should be moved to the refrigerator which will slow down the ripening process. Whole ripe mangos may be stored for up to five days in the refrigerator.</li>
  <li>Mangos may be peeled, cubed and placed in an airtight container in the refrigerator for several days. You can also store them in the freezer for up to six months.</li>
  </ol>
          
  <strong class="hdbold">Handling and Cutting Mangos</strong>
  <ol>
  <li>Always use a clean knife and cutting board to cut a mango.</li>
  <li>If you have handled or cut any type of meat or seafood, ALWAYS sanitize your hands, work area, utensils and cutting board before handling or cutting any fruits or vegetables, including mangos.</li>
  <li>Always wash mangos before cutting.</li>
  <li> Check out our www.savanifarms.com website for step-by-step instructions on how to cut mango. </li>
