        <td align="left" valign="middle">Farm-to Fork aims at eliminating the middleman completely from the mango supply chain and bring the produce directly from farmers to consumers. It aims at helping farmers get a fair price for their hard work and thus help them sustain themselves and their families and live a better quality life.</td>
