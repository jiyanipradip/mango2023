        <td height="30" align="left" valign="middle" class="hdbold">CORPORATE  SOCIAL RESPONSIBILITY</td>
      </tr>
      <tr>
        <td align="left" valign="middle">It has long been the dream of Savani Farms to make efforts towards improving the socio-economic standards of the underprivileged farmers in India.  Mango trade in India is heavily controlled by contractors and middlemen. The small farmers that depend on them for the sale of their mangoes end up receiving a very small portion of the profit. They are forced to live on this income until the next harvest. It is this distraught, socio-economic standard of the Indian farmers that led Savani Farms to take initiatives in trying to eradicate their poverty and help them get a fair price for their produce. </td>
      </tr>
      <tr>
        <td align="left" valign="middle">&nbsp;</td>
      </tr>
