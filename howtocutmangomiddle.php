    <div class="row">
      <div class="col-md-9">
          <h4>Using a Mango Splitter</h4>
        <p>Stand the mango on your cutting board stem end down and hold. Center the Mango Splitter above the mango, with the opening in the splitter aligned with the widest part of the mango.   </p>
      </div>
