      <div class="col-md-9">
          <p></p>
        <p>Cut parallel slices into the mango flesh, being careful not to cut through the skin. Turn the mango cheek 1/4 rotation and cut another set of parallel slices to make a checkerboard pattern.</p>        
      </div>
