          <h4>"Inside Out" Mango Cutting Method</h4>
        <p>Stand the mango on your cutting board stem end down and hold. Place your knife about 1/4" from the widest center line and cut down through the mango. Flip the mango around and repeat this cut on the other side. The resulting ovals of mango flesh are known as the "cheeks". What's left in the middle is mostly the mango seed. </p>
      </div>
      <div class="col-md-3"><img src="imagess/how-to-cut/sliceandscoop_1.jpg"  class="about-img"></div>
